package slicktest;

/**
 * This class represent a rectangle found on a map. 
 * @author elythiel
 *
 */
public class Rectangle extends Object {
	
	private int id ;
	private float x ;
	private float y ;
	private float width ;
	private float height ;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	public float getWidth() {
		return width;
	}
	public void setWidth(float width) {
		this.width = width;
	}
	public float getHeight() {
		return height;
	}
	public void setHeight(float height) {
		this.height = height;
	}
	
}
